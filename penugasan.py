#pengisisan
nilai= 100
#penjumlahan
nilai+= 20
print(f"Nilai + 20 : {nilai}")
#pengurangan
nilai-=5
print(f"Nilai - 5 : {nilai}")
#perkalian
nilai*=5
print(f"Nilai * 5 : {nilai}")

#pembagian
nilai/=5
print(F"Nilai / 5 :{nilai}")
# exponesial
nilai**=5
print(F"Nilai ** 5 :{nilai}")
#modulus
nilai%=5
print(F"Nilai % 5 :{nilai}")
#devision for
nilai//=5
print(F"Nilai // 5 :{nilai}")
