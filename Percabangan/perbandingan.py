x = 10
y = 5
# 1. equal / sama dengan ==
print(f"{x} == {y} : {x==y} \n")
# 2. not equal (!=)
print(f"{x} != {y} : {x!=y} \n")
# greater than / lebih besar(>) 
print(f"{x} > {y} : {x>y} \n")
#less than lebih kecil(<)
print(f"{x} < {y} : {x<y} \n")
#less than or equal to lebih besar sama dengan
print(f"{x} >= {y} : {x>=y} \n")


